package com.alkan.author.response.work;

public class WorkApiEntry {
    private String key;
    private String title;

    public WorkApiEntry() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
